"""
  Python possui tipagem dinâmica / forte, ou seja, o tipo de dado é definido no momento da atribuição.
  123 -> int (inteiro). O Python já sabe que 123 é um inteiro.
  str -> string -> texto
"""

# Aspas simples
print('Vou passar no concurso!')

# Aspas duplas
print("Vou passar no concurso!")

# Escape
print("Vou passar no \"concurso\"!")
print('Vou passar no \'concurso\'!')

# r permite mostrar o escape
print(r'Vou passar no \'concurso\'!')
print(r"Vou passar no \n concurso!")

# Invertendo aspas
print('Vou passar no "concurso"!')
print("Vou passar no 'concurso'!")

"""
  Tipo int e float
"""

print(11)
print(-11)
print(0)

print(11.0)
print(-11.0)
print(0.0)

"""
  Tipo boolean
"""

print(10 == 10) # True
print(10 == 11) # False
