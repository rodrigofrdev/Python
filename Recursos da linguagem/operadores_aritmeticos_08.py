# Operadores aritméticos

adicao = 27 + 28
print('Adição:', adicao)

subtracao = 55 - 27
print('Subtração:', subtracao)

multiplicacao = 3 * 9
print('Multiplicação:', multiplicacao)

divisao = 27 / 3 # Sempre retorna um float
print('Divisão:', divisao)

divisao_inteira = 27 // 3 # Retorna um inteiro
print('Divisão inteira:', divisao_inteira)

exponenciacao = 3 ** 3
print('Exponenciação:', exponenciacao)

resto = 27 % 4
print('Resto:', resto)
print('10 é divisível por 2?', 10 % 2 == 0)